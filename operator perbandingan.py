a = 20
b = 10

#sama dengan
if a == b :
    print("benar")
#tidak sama dengan
if a != b :
    print("benar")
#lebih kecil
if a < b :
    print("benar")
#lebih besar
if a > b :
    print("benar")
#lebih kecil sama dengan
if a <= b :
    print("benar")
#lebih besar sama dengan
if a >= b :
    print("benar")
